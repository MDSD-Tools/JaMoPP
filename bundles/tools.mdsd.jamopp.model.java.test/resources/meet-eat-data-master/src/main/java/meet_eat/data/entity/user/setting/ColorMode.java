package meet_eat.data.entity.user.setting;

/**
 * Represents the different color modes of the application.
 */
public enum ColorMode {

    /**
     * The dark color concept of the application.
     */
    DARK,

    /**
     * The light color concept of the application.
     */
    LIGHT,

    /**
     * The adopted colour concept of the device for the application.
     */
    SYSTEM
}