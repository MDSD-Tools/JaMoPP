package teammates.ui.output;

/**
 * The feedback visibility type.
 */
public enum FeedbackVisibilityType {
    /**
     * General recipient.
     */
    RECIPIENT,

    /**
     * Giver's team member.
     */
    GIVER_TEAM_MEMBERS,

    /**
     * Recipient's team members.
     */
    RECIPIENT_TEAM_MEMBERS,

    /**
     * Students in the course.
     */
    STUDENTS,

    /**
     * Instructors in the course.
     */
    INSTRUCTORS,
}
